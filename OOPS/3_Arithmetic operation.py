class arithmetic:
    def sum(a,x,y):
        print(f"The sum of {x} ans {y} = ",x+y)
    def sub(a,x,y):
        print(f"The sum of {x} ans {y} = ",x-y)
    def mul(a,x,y):
        print(f"The sum of {x} ans {y} = ",x*y)
    def div(a,x,y):
        print(f"The sum of {x} ans {y} = ",x/y)
    def mod(a,x,y):
        print(f"The sum of {x} ans {y} = ",x%y)
obj = arithmetic()
obj.sum(5,5)
obj.sub(5,5)
obj.mul(5,5)
obj.div(5,5)
obj.mod(11,5)